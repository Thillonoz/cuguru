static int layouts[NUM_LAYOUTS][ROWS][COLS];

static inline void InitializeLayouts() {
    int idx = 0;
    for (int i = 0; i < NUM_BASE_LAYOUTS; i++) {
        for (int mirror = 0; mirror < 4; mirror++) {
            for (int y = 0; y < ROWS; y++) {
                for (int x = 0; x < COLS; x++) {
                    int src_y = y;
                    int src_x = x;
                    
                    if (mirror == 1 || mirror == 3) src_x = COLS - 1 - x;
                    if (mirror == 2 || mirror == 3) src_y = ROWS - 1 - y;
                    
                    layouts[idx][y][x] = base_layouts[i][src_y][src_x];
                }
            }
            idx++;
        }
    }
}

